
# For loop is a scpecial type ofloop in python that
# allows to loop through different collection of items

# for -> (specify a variable)
for letter in "banana cream":
    print(letter)
    
friends =["Nicat", "Nihat", "Fuad"] 
for friend in friends:
    print(friend)
    
# range operations in for loop    
for i in range(10):     # answer will be 1,2,...,9
    print(i)
    
for i in range(3, 10):  # (beginning, ending)
    print(i)

# length of array example:
friends =["Nicat", "Nihat", "Fuad"] 
print(len(friends))
for i in range(len(friends)):
    print(friends[i])

  
    